#include "initialisation.h"
#include <stdio.h>
#include <conio.h>
#include <graphics.h>
#include <easyx.h>

void initialisation(char arr[3][3])
{
	initgraph(600 , 600);
	drawboard();
	for (int i = 0; i < 3; i++)
	{
		for (int j = 0; j < 3; j++)
		{
			arr[i][j] = '#';
		}
	}
}
void drawboard()
{
	line(0, 200, 600, 200);
	line(0, 400, 600, 400);
	line(200, 0, 200, 600);
	line(400, 0, 400, 600);
}





