void drawpiece(char arr[3][3])
{
	for (int i = 0; i < 3; i++)
	{
		for (int j = 0; j < 3; j++)
		{
			if (arr[i][j] == 'X')
			{
				line(i * 200, j * 200, i * 200 + 200, j * 200 + 200);
				line(i * 200 + 200, j * 200, i * 200, j * 200 + 200);
			}
			if (arr[i][j] == 'O')
			{
				circle(200 * i + 100, 200 * j + 100, 100);
			}
		}
	}
	
}








